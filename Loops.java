public class Loops {
    public static void main(String[] args) {
        // for loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("For loop iteration: " + i);
        }

        // while loop
        int j = 1;
        while (j <= 5) {
            System.out.println("While loop iteration: " + j);
            j++;
        }

        // do-while loop
        int k = 1;
        do {
            System.out.println("Do-while loop iteration: " + k);
            k++;
        } while (k <= 5);
    }
}

